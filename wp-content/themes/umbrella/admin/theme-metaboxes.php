// get metabox
if ( ! function_exists( 'nk_get_metabox' ) ) :
    function nk_get_metabox( $name = null, $postId = null ) {
        $value = null;

        // try get value from meta box
        if ( function_exists( 'get_field' ) ) {
            if ( $postId == null ) {
                $postId = get_the_ID();
            }
            $value = get_field( $name, $postId );
        }

        return $value;
    }
